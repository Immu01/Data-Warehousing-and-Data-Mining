age <- c(23, 23, 27, 27, 39, 41, 47, 49, 50, 52, 54, 54, 56, 57, 58, 58, 60, 61)
fat <- c(9.5, 26.5, 7.8, 17.8, 31.4, 25.9, 27.4, 27.2, 31.2, 34.6, 42.5, 28.8, 33.4, 30.2, 34.1, 32.9, 41.2, 35.7)
age_mean <- mean(age)
age_median <- median(age)
age_sd <- sd(age)
fat_mean <- mean(fat)
fat_median <- median(fat)
fat_sd <- sd(fat)
cat("Age:\n")
cat("Mean:", age_mean, "\n")
cat("Median:", age_median, "\n")
cat("Standard Deviation:", age_sd, "\n\n")
cat("Fat:\n")
cat("Mean:", fat_mean, "\n")
cat("Median:", fat_median, "\n")
cat("Standard Deviation:", fat_sd, "\n\n")
boxplot(age, fat, main = "Boxplot of Age and Fat")
scatter.smooth(age, fat, main = "Scatter plot with Smoothed Line", xlab = "Age", ylab = "Fat")
qqplot(age, fat, main = "QQ Plot: Age vs. Fat")